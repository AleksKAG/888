// TestGetByClient проверяет получение посылок по идентификатору клиента
func TestGetByClient(t *testing.T) {
	// prepare
	db, err := sql.Open("sqlite", "tracker.db") // настройте подключение к БД
	if err != nil {
		fmt.Println(err)
		return
	}
	defer db.Close()

	store := NewParcelStore(db)

	parcels := []Parcel{
		getTestParcel(),
		getTestParcel(),
		getTestParcel(),
	}
	parcelMap := map[int]Parcel{}

	// задаём всем посылкам один и тот же идентификатор клиента
	client := randRange.Intn(10_000_000)
	parcels[0].Client = client
	parcels[1].Client = client
	parcels[2].Client = client

	// add
	for i := 0; i < len(parcels); i++ {
		id, err := store.Add(parcels[i]) // добавьте новую посылку в БД, убедитесь в отсутствии ошибки и наличии идентификатора
		require.NoError(t, err)
		require.NotNil(t, id)

		// обновляем идентификатор добавленной у посылки
		parcels[i].Number = id

		// сохраняем добавленную посылку в структуру map, чтобы её можно было легко достать по идентификатору посылки
		parcelMap[id] = parcels[i]
	}

	// get by client
	storedParcels, err := store.GetByClient(client) // получите список посылок по идентификатору клиента, сохранённого в переменной client
	// убедитесь в отсутствии ошибки
	require.NoError(t, err)
	// убедитесь, что количество полученных посылок совпадает с количеством добавленных
	require.Equal(t, len(storedParcels), len(parcels))
	// check
	for _, storedParcel := range storedParcels {
		// убедитесь, что посылка содержится в parcelMap
		_, found := parcelMap[storedParcel.Number]
		require.True(t, found, "stored parcel not found in parcelMap")

		// получите ожидаемую посылку из parcelMap
		expectedParcel, ok := parcelMap[storedParcel.Number]
		require.True(t, ok, "expected parcel not found in parcelMap")

		// убедитесь, что значения полей полученной посылки совпадают с ожидаемыми значениями
		require.Equal(t, expectedParcel.Client, storedParcel.Client, "client mismatch")
		require.Equal(t, expectedParcel.Address, storedParcel.Address, "address mismatch")
		require.Equal(t, expectedParcel.Status, storedParcel.Status, "status mismatch")
		require.Equal(t, expectedParcel.CreatedAt, storedParcel.CreatedAt, "created_at mismatch")
	}
}
